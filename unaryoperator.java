public class unaryoperator {
    public static void main(String[] args) {
        int p = 5, q = 5;
        System.out.println(p++);
        System.out.println(p);
        System.out.println(++p);
        System.out.println(p);
        System.out.println(q++);
        System.out.println(q);
        System.out.println(++q);
        System.out.println(q);

    }
    
}
